package com.jaypandit.bookapp.school;

public class School {

    private String schoolName;
    private String  schoolBoard;
    private String schoolBook;
    private String schoolArea;
    private String schoolBranch;
    private String schoolState;
    private String schoolCountry;

    public String getSchoolName() {
        return schoolName;
    }

    public School setSchoolName(String schoolName) {
        this.schoolName = schoolName;
        return this;
    }

    public String  getSchoolBoard() {
        return schoolBoard;
    }

    public School setSchoolBoard(String schoolBoard) {
        this.schoolBoard = schoolBoard;
        return this;
    }

    public String getSchoolBook() {
        return schoolBook;
    }

    public School setSchoolBook(String schoolBook) {
        this.schoolBook = schoolBook;
        return this;
    }

    public String getSchoolArea() {
        return schoolArea;
    }

    public School setSchoolArea(String schoolArea) {
        this.schoolArea = schoolArea;
        return this;
    }

    public String getSchoolBranch() {
        return schoolBranch;
    }

    public School setSchoolBranch(String schoolBranch) {
        this.schoolBranch = schoolBranch;
        return this;
    }

    public String getSchoolState() {
        return schoolState;
    }

    public School setSchoolState(String schoolState) {
        this.schoolState = schoolState;
        return this;
    }

    public String getSchoolCountry() {
        return schoolCountry;
    }

    public School setSchoolCountry(String schoolCountry) {
        this.schoolCountry = schoolCountry;
        return this;
    }
}
